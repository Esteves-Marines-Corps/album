function mostraGuilherme(el) {
    document.getElementById(el).style.display = 'block';

    if (display = 'block') {
        document.getElementById('Home').style.display = 'none';
        document.getElementById('Daniela').style.display = 'none';
        document.getElementById('Davi').style.display = 'none';
    }
}

